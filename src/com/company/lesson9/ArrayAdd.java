package com.company.lesson9;

public class ArrayAdd {

    public static void main(String[] args) {
        char a = 'y';
        switch (a) {
            case '3':
                System.out.println("5");
                break;
            case 'y':
                System.out.println("asa");
                break;
            default:
                System.out.println("basqa");
                break;
        }


    }
}
